package test;

public class OverridingTest {

	public static void main(String[] args) {
		Customer lee = new Customer(10010, "이순신");
		lee.bonusPoint = 1000;
		
		VIPCustomer kim = new VIPCustomer(10020,  "엄희진", 12345);
		kim.bonusPoint = 10000;
		
		int price = 10000;
		System.out.println(lee.getCusName() + "님이 지불해야 하는 금액은 " + lee.calcPrice(price) + "원입니다.");
		System.out.println(kim.getCusName() + "님이 지불해야 하는 금액은 " + kim.calcPrice(price) + "원입니다.");

	}

}
